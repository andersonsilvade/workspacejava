
public class Caracteristica {
	
	private int cc;
	private int ano;
	private int km;
	
	public Caracteristica(int cc,int ano,int km){
		this.cc = cc;
		this.ano = ano;
		this.km = km;
	}

	public int getCc() {
		return cc;
	}

	public void setCc(int cc) {
		this.cc = cc;
	}

	public int getAno() {
		return ano;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

	public int getKm() {
		return km;
	}

	public void setKm(int km) {
		this.km = km;
	}
	
	

}
