
public class Embalagem {
	
	private String embalagem;
	private int quantEmbalagem;

	public Embalagem(String embalagem,int quantEmbalagem) {
		
		this.embalagem = embalagem;
		this.quantEmbalagem = quantEmbalagem;
	}

	public String getEmbalagem() {
		return embalagem;
	}

	public void setEmbalagem(String embalagem) {
		this.embalagem = embalagem;
	}

	public int getQuantEmbalagem() {
		return quantEmbalagem;
	}

	public void setQuantEmbalagem(int quantEmbalagem) {
		this.quantEmbalagem = quantEmbalagem;
	}

	

	
}
