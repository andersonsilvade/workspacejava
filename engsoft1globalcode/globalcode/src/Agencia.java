
public class Agencia {

	
private	String numero;
private	int banco;

public  Agencia(String numero,int banco){
	this.numero = numero;
	this.banco = banco;
}

public String getNumero() {
	return numero;
}
public void setNumero(String numero) {
	this.numero = numero;
}
public int getBanco() {
	return banco;
}
public void setBanco(int banco) {
	this.banco = banco;
}

}
