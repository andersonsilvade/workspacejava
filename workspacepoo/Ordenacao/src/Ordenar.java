import java.util.Set;
import java.util.TreeSet;


public class Ordenar {
	
	   
	
	public static void main(String[] args) {
		
		Set<String>nome = new TreeSet<String>();
		nome.add("Roberta");
		nome.add("Anderson");
		nome.add("Mauro");
		nome.add("Maria");
		
		
		for(Object i : nome){
			
			System.out.println(i);
		}
		
		
			

	}

}
